public class Pattern {
    public static void main(String[] args ){

        int n = 1;
        int a = 1;
        while (a <= 10) {
            System.out.print(a + " \n");
            a++;
        }
        System.out.println();

        while ( n <= 10 ){
            System.out.print("* \n");
            n++;
        }
    }
}
